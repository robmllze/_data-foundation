@GenerateModel(
  shouldInherit: true,
  fields: {
    ('name', String),
  },
)
abstract class _ModelEnum extends ThisModel<ModelEnum> {
  //
  //
  //

  bool operator ==(Object other) => this.equals(other);

  //
  //
  //

  @override
  bool equals(dynamic other) {
    if (other is Enum) {
      return this.model.name == other.name;
    }
    return super.equals(other);
  }
}

