extension EventPoolExtension<TModel extends ModelEvent> on Iterable<TModel> {
  //
  //
  //

  // --- Filtering -------------------------------------------------------------

  /// Filters-out events that are either expired, archived or hidden for the
  /// user or entity identified by [forId].
  Iterable<ModelEvent> filterClosedEventsFor({required String forId}) {
    return this.where((e) => !e.isExpired() && !e.isArchivedBy(forId) && !e.isHiddenBy(forId));
  }
}
